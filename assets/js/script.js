// Show How to Play pop up
function displayHow() {
    let showHow = document.getElementById('how');
    let hideHow = document.getElementById('start-quiz');
    if (hideHow.style.display = 'none') {
        showHow.style.display = 'flex';
    } else {
        showHow.style.display = 'none';
    }
}

